--3) Mostrar o nome dos alunos com a 1a, 2a e 3a maior quantidade de locações de livros sobre PostgreSQL nos últimos 9 meses.s
    
  select usuario.nome
  from locacao
  			join exemplar on exemplar.codigo = locacao.exemplar
  			join livro on livro.codigo = exemplar.livro
  			join usuario on usuario.codigo = locacao.usuario
  where usuario.tipo = 'A' and 
  			lower(livro.titulo) like '%postgresql%' and
  			locacao.retirada >= now()-'9 months'::interval
  group by usuario.codigo
  having count(*) in (select distinct(count(*)) as quantidade
  									  from locacao
  									  join exemplar on exemplar.codigo = locacao.exemplar
											join livro on livro.codigo = exemplar.livro
											join usuario on usuario.codigo = locacao.usuario
								where usuario.tipo = 'A' and 
											lower(livro.titulo) like '%postgresql%' and
											locacao.retirada >= now()-'9 months'::interval
								group by usuario.codigo 
								order by 1 desc
								limit 3);


