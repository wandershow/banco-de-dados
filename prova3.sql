      or ((usuario.tipo = 'F') and locacao.retirada >= now()-'3 months'::interval and ((locacao.entrega is not null and locacao.entrega-locacao.retirada > 14) or (locacao.entrega is null and now()::date-locacao.retirada > 14)))
      or ((usuario.tipo = 'P') and locacao.retirada >= now()-'3 months'::interval and ((locacao.entrega is not null and locacao.entrega-locacao.retirada > 21) or (locacao.entrega is null and now()::date-locacao.retirada > 21))) 
      group by 1
     order by 1 asc;
