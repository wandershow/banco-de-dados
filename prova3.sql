-- Sabendo que o prazo de locação é de 21 dias para professores, 14 dias para funcionários e 7 dias para alunos:
--1) Mostrar a quantidade de alunos por curso que não atrasaram entrega de livros nos últimos 3 meses.
       
--funcionando
	select usuario.curso, count(*) as quantidade
	from usuario
	where usuario.tipo = 'A' and
		usuario.codigo not in (select locacao.usuario
													from locacao 
														join usuario on locacao.usuario = usuario.codigo
													where usuario.tipo = 'A' and 
														locacao.retirada >= now()-'3 months'::interval and
														((locacao.entrega is not null and locacao.entrega-locacao.retirada > 7) or (locacao.entrega is null and now()::date-locacao.retirada > 7)))
	group by usuario.curso;
    
    
--2) Mostrar, de duas formas diferentes, o nome dos usuários que atrasaram entrega de livros nos últimos 3 meses.
    ((select usuario.nome
            from locacao 
                join usuario on locacao.usuario = usuario.codigo 
                where (usuario.tipo = 'A') and
                 locacao.retirada >= now()-'3 months'::interval and
                  ((locacao.entrega is not null and locacao.entrega-locacao.retirada > 7) or (locacao.entrega is null and now()::date-locacao.retirada > 7)))
union
    (select usuario.nome
            from locacao 
            join usuario on locacao.usuario = usuario.codigo 
            where (usuario.tipo = 'P') and
            locacao.retirada >= now()-'3 months'::interval and ((locacao.entrega is not null and locacao.entrega-locacao.retirada > 21) or (locacao.entrega is null and now()::date-locacao.retirada > 21)))
union
    (select usuario.nome 
             from locacao 
             join usuario on locacao.usuario = usuario.codigo 
             where (usuario.tipo = 'F') and locacao.retirada >= now()-'3 months'::interval and ((locacao.entrega is not null and locacao.entrega-locacao.retirada > 14) or (locacao.entrega is null and now()::date-locacao.retirada > 14)))) 
    order by 1;  
    
